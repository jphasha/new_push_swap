#include "libft.h"

void	ft_rev_print(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	str[i] = '\0';
	while (i--)
	{
		ft_putchar(str[i]);
	}
}
